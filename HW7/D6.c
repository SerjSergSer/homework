#include <stdio.h>

// Рекурсивная функция для чтения и печати строки наоборот
void reverse_string() {
    char c;
    scanf("%c", &c);
    
    if (c != '.') {
        // Рекурсивно читаем следующий символ
        reverse_string();
        // После возврата из рекурсии печатаем символы в обратном порядке
        printf("%c", c);
    }
    // Когда встречаем точку - просто возвращаемся (не печатаем её)
}

int main() {
    // Вызываем рекурсивную функцию
    reverse_string();
    printf("\n");
    
    return 0;
}
