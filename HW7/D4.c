#include <stdio.h>

// Рекурсивная функция для вывода цифр числа в прямом порядке
void print_num(int num) {
    if (num < 10) {
        // Если число однозначное, просто выводим его
        printf("%d", num);
    } else {
        // Сначала выводим все цифры кроме последней
        print_num(num / 10);
        // Затем выводим последнюю цифру с пробелом
        printf(" %d", num % 10);
    }
}

int main() {
    int N;
    
    // Ввод числа
    scanf("%d", &N);
    
    // Особый случай: если число 0
    if (N == 0) {
        printf("0");
    } else {
        // Вызов рекурсивной функции
        print_num(N);
    }
    
    return 0;
}
